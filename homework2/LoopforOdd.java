// List of Odd number using for and ++

package homework2;

public class LoopforOdd {
    public static void main(String[] args) {

        System.out.println("List of Odd numbers ");
        for (int i = 1; i <= 99; i++) {

            if (i % 2 != 0)

                System.out.println(i);
        }
    }
}
